#!/usr/bin/env python3
"""
Generate src/bdsim/_blockdiagram_mixin.py

This mixin class holds stub signatures for all dynamically-injected block
factory methods.  BlockDiagram inherits from it so that Pylance / mypy can
see the factory methods without needing a .pyi stub file.

The stubs are never called at runtime: the @block decorator replaces them on
the BlockDiagram class when each block module is imported.

Usage:
    python src/bdsim/bin/stubgen.py
"""

from __future__ import annotations

import inspect
from math import inf
from pathlib import Path

import bdsim

OUTPUT = Path(__file__).parent.parent / "_blockdiagram_mixin.py"

header = '''\
# This file is AUTO-GENERATED by src/bdsim/bin/stubgen.py.
# Do not edit by hand – run stubgen.py to regenerate.
from __future__ import annotations

from math import inf
from typing import Any, Callable, Literal
import numpy as np
from spatialmath import SE3, Twist3

Vector1D = int | float | tuple[float, ...] | list[float] | np.ndarray

class BlockDiagramMixin:
    """
    Mixin that declares stub signatures for all block factory methods.

    ``BlockDiagram`` inherits from this class so that static type checkers
    (Pylance, mypy) can resolve calls like ``bd.STEP()``, ``bd.SCOPE()``, etc.

    At runtime the ``@block`` decorator replaces every stub with the real
    factory function, so these bodies are never executed.
    """
'''


def _find_matching_bracket(text: str, start: int) -> int:
    depth = 0

    for index in range(start, len(text)):
        char = text[index]
        if char == "[":
            depth += 1
        elif char == "]":
            depth -= 1
            if depth == 0:
                return index

    raise ValueError(f"unmatched '[' in {text!r}")


def _split_top_level_args(text: str) -> list[str]:
    args: list[str] = []
    start = 0
    square_depth = 0
    paren_depth = 0
    brace_depth = 0

    for index, char in enumerate(text):
        if char == "[":
            square_depth += 1
        elif char == "]":
            square_depth -= 1
        elif char == "(":
            paren_depth += 1
        elif char == ")":
            paren_depth -= 1
        elif char == "{":
            brace_depth += 1
        elif char == "}":
            brace_depth -= 1
        elif char == "," and square_depth == paren_depth == brace_depth == 0:
            args.append(text[start:index].strip())
            start = index + 1

    args.append(text[start:].strip())
    return args


def _modernize_typing_syntax(text: str) -> str:
    text = text.replace("NoneType", "None")

    while True:
        optional_index = text.find("Optional[")
        union_index = text.find("Union[")
        candidates = [index for index in (optional_index, union_index) if index >= 0]

        if not candidates:
            return text

        start = min(candidates)
        token = "Optional" if start == optional_index else "Union"
        inner_start = start + len(token)
        inner_end = _find_matching_bracket(text, inner_start)
        inner = _modernize_typing_syntax(text[inner_start + 1 : inner_end])

        if token == "Optional":
            replacement = f"{inner} | None"
        else:
            replacement = " | ".join(
                _modernize_typing_syntax(part) for part in _split_top_level_args(inner)
            )

        text = text[:start] + replacement + text[inner_end + 1 :]


sim = bdsim.BDSim()

with open(OUTPUT, "w") as f:
    print(f"writing mixin --> {OUTPUT}")
    f.write(header)

    assert sim._blocklibrary, "No blocks found in library"

    for block, info in sorted(sim._blocklibrary.items()):
        meth = info["class"]
        sig = inspect.signature(meth.__init__)

        # Build parameter list, skipping 'self' from __init__ and ensuring
        # **blockargs is present.
        params = [p for p in sig.parameters.values() if p.name != "self"]
        has_blockargs = any(p.kind == inspect.Parameter.VAR_KEYWORD for p in params)
        if not has_blockargs:
            params.append(inspect.Parameter("blockargs", inspect.Parameter.VAR_KEYWORD))

        # Normalize legacy typing spellings that come back from inspect so the
        # generated mixin uses the project's modern ``|`` style annotations.
        sig_str = (
            "("
            + ", ".join(["self"] + [_modernize_typing_syntax(str(p)) for p in params])
            + ")"
        )
        doc = (meth.__init__.__doc__ or "").rstrip()

        f.write(f"\n    # {info['module']}.{info['classname']}\n")
        f.write(f"    def {block}{sig_str} -> Any:\n")
        if doc:
            # Escape backslashes so LaTeX sequences like \omega don't produce
            # SyntaxWarnings in the generated file (Python 3.12+).
            safe_doc = doc.replace("\\", "\\\\")
            f.write(f'        """{safe_doc}\n        """\n')
        f.write("        ...\n")
