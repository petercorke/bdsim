#!/usr/bin/env python3
"""
Generate src/bdsim/_blockdiagram_mixin.py

This mixin class holds stub signatures for all dynamically-injected block
factory methods.  BlockDiagram inherits from it so that Pylance / mypy can
see the factory methods without needing a .pyi stub file.

The stubs are never called at runtime: the @block decorator replaces them on
the BlockDiagram class when each block module is imported.

Usage:
    python src/bdsim/bin/stubgen.py
"""

from __future__ import annotations

import inspect
from pathlib import Path
from math import inf

import bdsim

OUTPUT = Path(__file__).parent.parent / "_blockdiagram_mixin.py"

header = '''\
# This file is AUTO-GENERATED by src/bdsim/bin/stubgen.py.
# Do not edit by hand – run stubgen.py to regenerate.
from __future__ import annotations

from math import inf
from typing import Any, Optional, Union, Callable, Literal
import numpy as np
from spatialmath import SE3, Twist3

Vector1D = Union[int, float, tuple[float, ...], list[float], np.ndarray]

class BlockDiagramMixin:
    """
    Mixin that declares stub signatures for all block factory methods.

    ``BlockDiagram`` inherits from this class so that static type checkers
    (Pylance, mypy) can resolve calls like ``bd.STEP()``, ``bd.SCOPE()``, etc.

    At runtime the ``@block`` decorator replaces every stub with the real
    factory function, so these bodies are never executed.
    """
'''

sim = bdsim.BDSim()

with open(OUTPUT, "w") as f:
    print(f"writing mixin --> {OUTPUT}")
    f.write(header)

    assert sim._blocklibrary, "No blocks found in library"

    for block, info in sorted(sim._blocklibrary.items()):
        meth = info["class"]
        sig = inspect.signature(meth.__init__)

        # Build parameter list, skipping 'self' from __init__ and ensuring
        # **blockargs is present.
        params = [p for p in sig.parameters.values() if p.name != "self"]
        has_blockargs = any(p.kind == inspect.Parameter.VAR_KEYWORD for p in params)
        if not has_blockargs:
            params.append(inspect.Parameter("blockargs", inspect.Parameter.VAR_KEYWORD))

        # NoneType is Python's internal name for type(None); replace with None
        # so the generated file is valid (e.g. Union[int, NoneType] -> Union[int, None]).
        sig_str = (
            "("
            + ", ".join(["self"] + [str(p).replace("NoneType", "None") for p in params])
            + ")"
        )
        doc = (meth.__init__.__doc__ or "").rstrip()

        f.write(f"\n    # {info['module']}.{info['classname']}\n")
        f.write(f"    def {block}{sig_str} -> Any:\n")
        if doc:
            # Escape backslashes so LaTeX sequences like \omega don't produce
            # SyntaxWarnings in the generated file (Python 3.12+).
            safe_doc = doc.replace("\\", "\\\\")
            f.write(f'        """{safe_doc}\n        """\n')
        f.write("        ...\n")
